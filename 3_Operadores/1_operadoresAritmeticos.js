//Operadores Aritmeticos 

let a=3, b=2;
let z= a + b;
console.log("Resultado de la suma es: " + z);

z= a - b;
console.log("Resultado de la resta es: " + z);

z= a * b;
console.log("Resultado de multiplicar es: " + z);

z= a / b;
console.log("Resultado de la divición es: " + z);

z= a % b;// residuo de la divición
console.log("Resultado del modulo es: " + z);

z= a ** b;
console.log("Resultado del operador exponente es: " + z);